/**
* @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
*/
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	
	/* UART in mode Receiver ---------------------------------------------------*/
	if(huart1.RxState == HAL_UART_STATE_BUSY_RX)
	{
		if((GSM_Service_Find_OK_Response(gsm_module_received_data,(huart1.RxXferSize - huart1.RxXferCount))) || 
			(GSM_Service_Find_Error_Response(gsm_module_received_data,(huart1.RxXferSize - huart1.RxXferCount))) ||
			(GSM_Service_Find_SMS_Send_Start_Command(gsm_module_received_data,(huart1.RxXferSize - huart1.RxXferCount))))
		{
			__HAL_UART_DISABLE_IT(&huart1, UART_IT_RXNE);

			  /* Disable the UART Parity Error Interrupt */
			  __HAL_UART_DISABLE_IT(&huart1, UART_IT_PE);

			  /* Disable the UART Error Interrupt: (Frame error, noise error, overrun error) */
			  __HAL_UART_DISABLE_IT(&huart1, UART_IT_ERR);

			 /* Rx process is completed, restore huart->RxState to Ready */
			  huart1.RxState = HAL_UART_STATE_READY;

			  HAL_UART_RxCpltCallback(&huart1);
		}
  }
	
  /* USER CODE END USART1_IRQn 1 */
}

